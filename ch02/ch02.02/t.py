# По пути домой родители снова решили сыграть с детьми в поиск зверушек.
# Но на этот раз задача сложнее — нужно не просто найти зайку, а выбрать
# правильную строку по определённым условиям.

# Вам предстоит проверить три строки, найти среди них ту, где встречается
# слово "зайка", и вывести её вместе с её длиной. Если таких строк несколько,
# выберите наименьшую в алфавитном порядке.

# Для этого используйте составные условные операции, чтобы одновременно учесть
# несколько условий.

# Формат ввода
# Три строки описывающих придорожную местность.

# Формат вывода
# Строка в которой есть зайка, а затем её длина.
# Если таких строк несколько, выбрать ту, что меньше всех лексикографически.

# Подсказка
# Пример 1
# Ввод
# березка елочка зайка волк березка
# сосна сосна сосна елочка грибочки медведь
# сосна сосна сосна белочка сосна белочка
# Вывод
# березка елочка зайка волк березка 33
# Пример 2
# Ввод
# зайка березка
# березка зайка
# березка елочка березка
# Вывод
# березка зайка 13

candidates = []
for _ in range(3):
    s = input().strip()
    words = s.split()
    if "зайка" in words:
        candidates.append(s)

result = min(candidates)
print(f"{result} {len(result)}")
