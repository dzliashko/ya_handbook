# В давние времена верили, что если местность имеет форму треугольника, то в
# ней может скрываться страшное зло.

# При этом риск столкнуться с ним зависел от формы треугольника:

# Остроугольный — вероятность встречи со злом крайне мала.
# Тупоугольный — опасность велика.
# Прямоугольный — вероятность 100%!

# Напишите программу, которая определяет вероятность встретить зло по длинам
# сторон треугольника. Воспользуйтесь для этого составными условными операциями.

# Формат ввода
# Три числа — длины сторон треугольной местности.

# Формат вывода
# Вероятность встретить зло согласно поверью:

# крайне мала;
# велика;
# 100%.
# Подсказка
# Пример 1
# Ввод
# 3
# 5
# 4
# Вывод
# 100%
# Пример 2
# Ввод
# 6
# 3
# 4
# Вывод
# велика

a = float(input())
b = float(input())
c = float(input())

sides = sorted([a, b, c])
x, y, z = sides

x_sq = x * x
y_sq = y * y
z_sq = z * z

if abs(x_sq + y_sq - z_sq) < 1e-9:
    print("100%")
elif x_sq + y_sq < z_sq:
    print("велика")
else:
    print("крайне мала")
