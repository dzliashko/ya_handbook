# Математика, "простые числа" и цикл while.

# "Простые числа" не поддаются формализации и до сих пор изучаются учеными по
# всему миру.
# Они применяются в вычислительной техники, как основа для алгоритмов
# шифрования данных.

# Напишите программу для разложения числа на простые множители.

# Формат ввода
# Вводится одно натуральное число.

# Формат вывода
# Требуется составить математическое выражение – произведение простых
# неубывающих чисел, которое в результате дает исходное.

# Подсказка
# Пример 1
# Ввод
# 120
# Вывод
# 2 * 2 * 2 * 3 * 5
# Пример 2
# Ввод
# 98
# Вывод
# 2 * 7 * 7


n = int(input().strip())
factors = []

while n % 2 == 0:
    factors.append(2)
    n //= 2

d = 3
while d * d <= n:
    while n % d == 0:
        factors.append(d)
        n //= d
    d += 2

if n > 1:
    factors.append(n)

print(" * ".join(map(str, factors)))
