# Ваша задача — написать программу, которая угадывает загаданное целое число
# от 1 до 1000 с использованием цикла while.

# Пользователь (или тестирующая система) загадывает число и не сообщает его
# рограмме.
# Угадать число нужно не более чем за 10 попыток.

# На каждую попытку пользователь отвечает одной из фраз:

# Больше
# Меньше
# Угадал!
# Пример
# Ввод
# Загаданное число:
# 123
# Вывод
# Число отгадано

low = 1
high = 1000
attempts = 0
guessed = False

while attempts < 10 and not guessed:
    mid = (low + high) // 2
    print(mid)
    response = input().strip()

    if response == "Угадал!":
        print("Число отгадано")
        guessed = True
    elif response == "Больше":
        low = mid + 1
    elif response == "Меньше":
        high = mid - 1

    attempts += 1
